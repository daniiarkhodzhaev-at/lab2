#!/usr/bin/python3

import turtle

def main() -> int:
    turtle.shape("turtle")
    turtle.forward(50)
    turtle.left(90)
    turtle.forward(50)
    turtle.left(90)
    turtle.forward(50)
    turtle.right(90)
    turtle.forward(50)
    turtle.right(90)
    turtle.forward(50)
    return 0

if (__name__ == "__main__"):
    main()
